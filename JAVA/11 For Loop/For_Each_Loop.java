public class For_Each_Loop {
    public static void main(String args[]){
        int numbers[]={10,20,30,20,15};

        for(int n:numbers){
            System.out.println(n);
        }
    }
}


/*

A for-each loop is a type of loop that is used to iterate over a collection of elements. It is also known as an enhanced for loop.

Syntax
for (type variableName : arrayName) {
  // code block to be executed
}
*/