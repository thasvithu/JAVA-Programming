public class Relational{
    public static void main(String args[]){
        int a = 50, b = 25;

        System.out.println("Equal to                 : " + (a == b));
        System.out.println("Not Equal to             : " + (a != b));
        System.out.println("Greater than             : " + (a > b));
        System.out.println("Greater than or equal to : " + (a >= b));
        System.out.println("Less than                : " + (a < b));
        System.out.println("Less than or equal to    : " + (a <= b));
    }
}


/*

Relational operators in Java are used to comparing two variables for equality, non-equality, greater than, less than, etc. 
They return a boolean value - true or false. 
Relational operators are simple and easy to understand, making it easy to write code that performs conditional checks in if-else statements and so on. 
They can be used to compare values of different data types.

    Operator	Name	                    Example
    ==  	    Equal to	                x == y	
    !=  	    Not equal	                x != y	
    >	        Greater than	            x > y	
    <	        Less than	                x < y	
    >=  	    Greater than or equal to	x >= y	
    <=  	    Less than or equal to	    x <= y
*/